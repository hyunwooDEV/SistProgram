package com.sist.dto;

public class RecommendDTO {
	
	private String seq;
	private String studenNum;
	private String companyNum;
	private String recodate;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getStudenNum() {
		return studenNum;
	}
	public void setStudenNum(String studenNum) {
		this.studenNum = studenNum;
	}
	public String getCompanyNum() {
		return companyNum;
	}
	public void setCompanyNum(String companyNum) {
		this.companyNum = companyNum;
	}
	public String getRecoDate() {
		return recodate;
	}
	public void setRecoDate(String recodate) {
		this.recodate = recodate;
	}
	
	

}
