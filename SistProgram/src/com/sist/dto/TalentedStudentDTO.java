package com.sist.dto;

public class TalentedStudentDTO {
	
	private String seq;
	private String protfolio;
	private String reason;
	private String regiNum;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getProtfolio() {
		return protfolio;
	}
	public void setProtfolio(String protfolio) {
		this.protfolio = protfolio;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getRegiNum() {
		return regiNum;
	}
	public void setRegiNum(String regiNum) {
		this.regiNum = regiNum;
	}
	
	

}
